/*

adição: +
subtração: - 
multiplicação: *
divisão: /
incremento: ++
decremento: --

*/

//adição
let a = 10;
let b = 5;
let soma = a + b;
console.log(soma)

//subtração

let d = 10;
let e = 5;
let subtracao = d - e;
console.log(subtracao)

//multiplicação

let f = 10;
let g = 5;
let multiplicacao = f * g;
console.log(multiplicacao)

//divisao

let h = 10;
let i = 5;
let divisao = h / i;
console.log(divisao)

//incremento = soma mais 1

let j = 10;
j++;
console.log(j)

//decremento = subtrai menos 1

let m = 10;
m--;
console.log(m)

//atribuicao

let n = 12;
//n = n + 25;
n += 25
console.log(n)

let o = 12;
//o = o - 25;
o -= 25;
console.log(o)

